class Dataset_ETT_hour(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h'):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path)) #(17420, 8)

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len] #[0, 8208, 11088]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24] #[8640, 11520, 14400]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:] #index:(7,)
            df_data = df_raw[cols_data] #(17420,7)
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]] #(8640, 7)
            # fit()方法的作用是计算训练数据集中每个特征的均值和标准差
            self.scaler.fit(train_data.values)
            # 对数据集进行标准化处理，即将数据集中的每个特征按照之前计算好的均值和标准差进行线性变换，使得数据集符合标准正态分布的特性（均值为0，标准差为1）。
            data = self.scaler.transform(df_data.values) #(17420, 7) [[-0.3.....,1.4.....][........]]
        else:
            # 不需要进行特征缩放（即self.scale为False），直接将DataFrame转换为NumPy数组形式，以便后续的数据处理和建模
            data = df_data.values

        # 选取名为 'date' 的列，然后根据给定的 border1 和 border2 对这个列进行切片，得到一个包含特定日期范围的子数据集 df_stamp
        df_stamp = df_raw[['date']][border1:border2] #(8640,1)
        # 将 df_stamp 中的 'date' 列的数据类型转换为 datetime 类型，以便后续对日期数据进行处理或分析。
        df_stamp['date'] = pd.to_datetime(df_stamp.date)

        # 根据不同的时间编码方式（self.timeenc）将日期数据转换为相应的特征表示，并将处理后的数据存储在 data_stamp 中
        if self.timeenc == 0:
            # 使用 apply() 函数对 'date' 列中的日期数据进行提取，分别获取月、日、星期几和小时，并赋值给新添加的列
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            # 将不包括 'date' 列的数据转换为 NumPy 数组形式
            data_stamp = df_stamp.drop(['date'], axis=1).values
        elif self.timeenc == 1:
            #提取年、月、日、小时、分钟等时间特征，并根据频率 freq 进行相应的处理和转换
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq) #(4,8640)
            data_stamp = data_stamp.transpose(1, 0) #(8640, 4) [[-0.5, 0.1666667, -0.5, -0.00136986],[..........]]

        self.data_x = data[border1:border2] #(8640, 7)
        self.data_y = data[border1:border2] #(8640, 7)
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end] #(432, 7)
        seq_y = self.data_y[r_begin:r_end] #(384, 7)
        seq_x_mark = self.data_stamp[s_begin:s_end] #(432, 4)
        seq_y_mark = self.data_stamp[r_begin:r_end] #(384, 4)

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


